impl Solution {
  pub fn best_seq_at_index(height: Vec<i32>, weight: Vec<i32>) -> i32 {
    let mut zip = height.into_iter().zip(weight.into_iter()).collect::<Vec<(i32, i32)>>();
    zip.sort_by(|x, y| {
      let x0 = &x.0;
      let x1 = &x.1;
      let y0 = &y.0;
      let y1 = &y.1;
      if x0 == y0 {
        return y1.cmp(x1);
      }
      x0.cmp(y0)
    });

    let mut stack: Vec<(i32, i32)> = vec![];
    for i in 0..zip.len() {
      let mut off = stack.partition_point(|&x| x.0 < zip[i].1);
      let cnt = if off == 0 { 1 } else { stack[off - 1].1 + 1 };
      stack.insert(off, (zip[i].1, cnt));
      off += 1;
      while off < stack.len() && stack[off].1 < cnt {
        stack.remove(off);
      }
    }

    stack.iter().map(|x| x.1).max().unwrap()
  }
}

