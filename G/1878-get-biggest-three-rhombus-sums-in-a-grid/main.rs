impl Solution {
  pub fn get_biggest_three(grid: Vec<Vec<i32>>) -> Vec<i32> {
    let mut buf: Vec<Vec<(i32, i32)>> = vec![vec![(0, 0); grid[0].len()]; grid.len()];
    for r in 0..grid.len() {
      for c in 0..grid[0].len() {
        buf[r][c].0 += grid[r][c]
          + if r > 0 && c < grid[0].len() - 1 {
            buf[r - 1][c + 1].0
          } else {
            0
          };
        buf[r][c].1 += grid[r][c]
          + if r > 0 && c > 0 {
            buf[r - 1][c - 1].1
          } else {
            0
          };
      }
    }

    let mut ans: Vec<i32> = vec![-1, -1, -1];
    for r in 0..grid.len() {
      for c in 0..grid[0].len() {
        let steps = (r + 1).min((grid[0].len() - 1 - c) / 2 + 1).min(grid.len() - r);
        (1..=steps).for_each(|step| {
          let bl2tr = buf[r][c].0
            - if step > r || c + step >= grid[0].len() {
              0
            } else {
              buf[r - step][c + step].0
            };
          let tl2br = buf[r][c + 2 * (step - 1)].1 - buf[r - (step - 1)][c + step - 1].1;
          let br2tl = buf[r + step - 1][c + step - 1].1 - buf[r][c].1;
          let tr2bl = buf[r + step - 1][c + step - 1].0
            - buf[r][c + 2 * (step - 1)].0
            - if step > 1 {
              grid[r + step - 1][c + step - 1]
            } else {
              0
            };

          let sum = bl2tr + tl2br + br2tl + tr2bl;
          ans = match (sum > ans[0], ans[0] > sum && sum > ans[1], ans[1] > sum && sum > ans[2]) {
            (true, _, _) => vec![sum, ans[0], ans[1]],
            (_, true, _) => vec![ans[0], sum, ans[1]],
            (_, _, true) => vec![ans[0], ans[1], sum],
            (_, _, _) => vec![ans[0], ans[1], ans[2]],
          };
        });
      }
    }
    if ans[2] == -1 {
      ans.pop();
    }
    if ans[1] == -1 {
      ans.pop();
    }
    ans
  }
}

