func intervalIntersection(firstList [][]int, secondList [][]int) [][]int {
	if len(firstList) == 0 || len(secondList) == 0 {
		return nil
	}
	ret := [][]int{}
	idx1, idx2 := 0, 0
	for idx1 < len(firstList) && idx2 < len(secondList) {
		if firstList[idx1][1] < secondList[idx2][0] {
			idx1++
			continue
		}
		if firstList[idx1][0] > secondList[idx2][1] {
			idx2++
			continue
		}
		start := firstList[idx1][0]
		if start < secondList[idx2][0] {
			start = secondList[idx2][0]
		}
		end := firstList[idx1][1]
		if end > secondList[idx2][1] {
			end = secondList[idx2][1]
			idx2++
		} else {
			idx1++
		}

		ret = append(ret, []int{start, end})
	}

	return ret
}

