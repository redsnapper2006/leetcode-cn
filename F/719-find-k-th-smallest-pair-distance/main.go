func smallestDistancePair(nums []int, k int) int {
	sort.Ints(nums)

	lo := 0
	hi := nums[len(nums)-1] - nums[0]
	for lo < hi {
		mDis := lo + (hi-lo)/2

		left := 0
		count := 0
		for i := 0; i < len(nums); i++ {
			for nums[i]-nums[left] > mDis {
				left++
			}
			count += i - left
		}
		if count >= k {
			hi = mDis
		} else {
			lo = mDis + 1
		}
	}

	return lo
}

func smallestDistancePairV2(nums []int, k int) int {
	ABS := func(x int) int {
		if x < 0 {
			return -x
		}
		return x
	}
	var buf []int
	for i := 0; i < len(nums); i++ {
		for j := i + 1; j < len(nums); j++ {
			buf = append(buf, ABS(nums[i]-nums[j]))
		}
	}

	var TOPN func(buf []int, k int) int
	TOPN = func(buf []int, k int) int {
		s, e := 0, len(buf)-1
		pivot := buf[0]
		for s < e {
			for s < e {
				if buf[s] > pivot {
					break
				}
				s++
			}
			for e > s {
				if buf[e] <= pivot {
					break
				}
				e--
			}
			if s < e {
				buf[s], buf[e] = buf[e], buf[s]
			}
		}
		if s < len(buf) && buf[s] < pivot {
			buf[0], buf[s] = buf[s], buf[0]
		}

		if s == k-1 {
			return buf[s]
		} else if s < k-1 {
			return TOPN(buf[s:], k-s)
		} else { // s > k-1
			return TOPN(buf[0:s], k)
		}
	}
	return TOPN(buf, k)
}

