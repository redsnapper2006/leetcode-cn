  pub fn non_special_count(l: i32, r: i32) -> i32 {
    let MAX: i32 = 31622;
    let mut dp: Vec<i32> = vec![0; MAX as usize + 1];
    (2..=MAX).for_each(|v| {
      let idx = v as usize;
      if dp[idx] == 0 {
        dp[idx] = dp[idx - 1] + 1;
        for j in (v * v..=MAX).step_by(v as usize) {
          dp[j as usize] = -1;
        }
      } else {
        dp[idx] = dp[idx - 1];
      }
    });

    r - l + 1 - (dp[(r as f64).sqrt() as usize] - dp[((l - 1) as f64).sqrt() as usize])
  }

