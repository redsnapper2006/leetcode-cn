func largestMagicSquare(grid [][]int) int {
	rows, cols, cross, backcross :=
		make([][]int, len(grid)),
		make([][]int, len(grid)),
		make([][]int, len(grid)),
		make([][]int, len(grid))

	for i := 0; i < len(grid); i++ {
		rows[i] = make([]int, len(grid[0]))
		cols[i] = make([]int, len(grid[0]))
		cross[i] = make([]int, len(grid[0]))
		backcross[i] = make([]int, len(grid[0]))
	}
	for i := 0; i < len(grid); i++ {
		sum := 0
		for j := 0; j < len(grid[0]); j++ {
			sum += grid[i][j]
			rows[i][j] = sum
		}
	}
	for i := 0; i < len(grid[0]); i++ {
		sum := 0
		for j := 0; j < len(grid); j++ {
			sum += grid[j][i]
			cols[j][i] = sum
		}
	}
	for i := 0; i < len(grid)+len(grid[0])-1; i++ {
		sum := 0
		for j := i; j >= 0; j-- {
			ni, nj := j, i-j
			if ni < len(grid) && ni >= 0 && nj >= 0 && nj < len(grid[0]) {
				sum += grid[ni][nj]
				backcross[ni][nj] = sum
			}
		}
	}
	for i := 0; i < len(grid[0]); i++ {
		sum := 0
		for j := 0; j < len(grid); j++ {
			ni, nj := j, i+j
			if ni < len(grid) && ni >= 0 && nj >= 0 && nj < len(grid[0]) {
				sum += grid[ni][nj]
				cross[ni][nj] = sum
			}
		}
	}
	for i := 1; i < len(grid); i++ {
		sum := 0
		for j := 0; j < len(grid[0]); j++ {
			ni, nj := i+j, j
			if ni < len(grid) && ni >= 0 && nj >= 0 && nj < len(grid[0]) {
				sum += grid[ni][nj]
				cross[ni][nj] = sum
			}
		}
	}

	ret := 1
	for i := 0; i < len(grid)-1; i++ {
		for j := 0; j < len(grid[0])-1; j++ {
			length := len(grid) - i
			if length > len(grid[0])-j {
				length = len(grid[0]) - j
			}
			for m := 1; m < length; m++ {
				isValid := true
				for k := 1; k <= m; k++ {
					if j > 0 {
						if rows[i][j+m]-rows[i][j-1] != rows[i+k][j+m]-rows[i+k][j-1] {
							isValid = false
							break
						}
					} else {
						if rows[i][j+m] != rows[i+k][j+m] {
							isValid = false
							break
						}
					}
				}
				if !isValid {
					continue
				}

				isValid = true
				for k := 1; k <= m; k++ {
					if i > 0 {
						if cols[i+m][j]-cols[i-1][j] != cols[i+m][j+k]-cols[i-1][j+k] {
							isValid = false
							break
						}
					} else {
						if cols[i+m][j] != cols[i+m][j+k] {
							isValid = false
							break
						}
					}
				}
				if !isValid {
					continue
				}

				ph, pv, pc, pbc := 0, 0, 0, 0
				if j > 0 {
					ph = rows[i][j-1]
				}
				if i > 0 {
					pv = cols[i-1][j]
				}
				if i > 0 && j > 0 {
					pc = cross[i-1][j-1]
				}
				if j > 0 && i+m < len(grid)-1 {
					pbc = backcross[i+m+1][j-1]
				}

				if rows[i][j+m]-ph == cols[i+m][j]-pv &&
					cols[i+m][j]-pv == cross[i+m][j+m]-pc &&
					cross[i+m][j+m]-pc == backcross[i][j+m]-pbc && m+1 > ret {
					ret = m + 1
				}
			}
		}
	}
	return ret
}

